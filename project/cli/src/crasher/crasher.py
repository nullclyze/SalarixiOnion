colors = {
  'reset': '\033[0m',
  'black': '\033[30m',
  'red': '\033[31m',
  'green': '\033[32m',
  'yellow': '\033[33m',
  'blue': '\033[34m',
  'magenta': '\033[35m',
  'cyan': '\033[36m',
  'white':'\033[37m'
}


def console(text: str, name: str, delay: float = 0.01):
  for char in text:
    color = 'white'

    if name == 'random':
      color = random.choice(list(colors.keys()))
    else:
      color = name

    print(colors[color] + char + colors['reset'], end='', flush=True)
    time.sleep(delay)

  print()


